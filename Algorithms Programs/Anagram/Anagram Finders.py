# Question2
# Question answer by Sort and Compare -------------------------------
def is_anagram(str1, str2):
    list_str1 = list(str1)
    list_str1.sort()
    list_str2 = list(str2)
    list_str2.sort()

    return list_str1 == list_str2


print(is_anagram("anagram", "nagaram"))
print(is_anagram("cat", "rat"))
